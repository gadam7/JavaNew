package gr.aueb.cf.ch5;

/**
 * Δηλώσεις και αρχικοποιήσεις Float και Double
 */
public class DeclarationsApp {

    public static void main(String[] args) {
        float f = 31.4F;    // Typecast από double σε float
        float age = 23F;    // Typecast από int σε float

        double d = 9.17;    // Δε χρειάζεται typecast, double σε double
        double price = 17D; // Typecast από int σε double
    }
}
